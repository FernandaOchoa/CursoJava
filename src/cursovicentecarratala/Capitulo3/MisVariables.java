package cursovicentecarratala.Capitulo3;

public class MisVariables {

    public static void main(String[] args) {
        //Declaracion de Variables
        
        //Enteras
        int primeravariable;
        //Decimales
        double decimal;
        //Cadenas
        String cadena;
        //Boolean
        boolean boleana;
        
        //Asignacion
        primeravariable = 3;
        decimal = 2.3;
        cadena = "Soy un texto";
        boleana = true;
         
        //Devolver variable
        System.out.println(primeravariable);
        System.out.println(decimal);
        System.out.println(cadena);
        System.out.println(boleana);
    }
}
